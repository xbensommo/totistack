import { FIELD_TYPES, defineCollection } from '@xbensommo/shard-provider';

/**
 * @file apps/crm/collections/crm_leads.collection.js
 * @description Shard-provider collection definition for CRM leads.
 */

const crmLeads = defineCollection({
  name: 'crm_leads',
  shard: { type: 'none' },
  schema: {
    firstName: { type: FIELD_TYPES.STRING, required: true, searchable: true },
    lastName: { type: FIELD_TYPES.STRING, required: true, searchable: true },
    fullName: { type: FIELD_TYPES.STRING, required: false, searchable: true, sortable: true },
    email: { type: FIELD_TYPES.STRING, required: false, searchable: true, filterable: true },
    phone: { type: FIELD_TYPES.STRING, required: false, filterable: true },
    company: { type: FIELD_TYPES.STRING, required: false, searchable: true, filterable: true },
    title: { type: FIELD_TYPES.STRING, required: false },
    source: { type: FIELD_TYPES.STRING, required: false, filterable: true, sortable: true },
    status: { type: FIELD_TYPES.STRING, required: false, filterable: true, sortable: true },
    score: { type: FIELD_TYPES.NUMBER, required: false, sortable: true, filterable: true },
    tags: { type: FIELD_TYPES.ARRAY, required: false },
    assignedTo: { type: FIELD_TYPES.STRING, required: false, filterable: true },
    team: { type: FIELD_TYPES.STRING, required: false, filterable: true },
    nextFollowUp: { type: FIELD_TYPES.TIMESTAMP, required: false, sortable: true, filterable: true },
    notes: { type: FIELD_TYPES.STRING, required: false },
    convertedOpportunityId: { type: FIELD_TYPES.STRING, required: false, filterable: true },
    lastActivityAt: { type: FIELD_TYPES.TIMESTAMP, required: false, sortable: true },
    createdBy: { type: FIELD_TYPES.STRING, required: false, filterable: true },
    createdAt: { type: FIELD_TYPES.TIMESTAMP, readonly: true, sortable: true, filterable: true },
    updatedAt: { type: FIELD_TYPES.TIMESTAMP, readonly: true, sortable: true },
  },
  writableFields: [
    'firstName',
    'lastName',
    'fullName',
    'email',
    'phone',
    'company',
    'title',
    'source',
    'status',
    'score',
    'tags',
    'assignedTo',
    'team',
    'nextFollowUp',
    'notes',
    'convertedOpportunityId',
    'lastActivityAt',
    'createdBy',
    'createdAt',
    'updatedAt',
  ],
  updateableFields: [
    'firstName',
    'lastName',
    'fullName',
    'email',
    'phone',
    'company',
    'title',
    'source',
    'status',
    'score',
    'tags',
    'assignedTo',
    'team',
    'nextFollowUp',
    'notes',
    'convertedOpportunityId',
    'lastActivityAt',
    'createdBy',
    'createdAt',
    'updatedAt',
  ],
  indexes: [
    { fields: ['status', 'createdAt'] },
    { fields: ['assignedTo', 'status'] },
    { fields: ['company', 'createdAt'] },
    { fields: ['nextFollowUp', 'status'] },
  ],
  search: {
    mode: 'token-array',
    fields: ['firstName', 'lastName', 'fullName', 'email', 'company'],
  },
  rules: {
    read: 'auth',
    create: 'auth',
    update: 'auth',
    delete: 'adminOrManager',
  },
});

export default crmLeads;
